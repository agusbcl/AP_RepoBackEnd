package com.portfolio.ab.Security.Controller;


public class Message {
    private String message;
    
    //Constructor
    
    public Message() {
    }

    public Message(String message) {
        this.message = message;
    }
    
    //Getter and Setter

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
    
    
}
